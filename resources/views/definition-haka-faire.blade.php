      <p>Haka est un nom générique pour toutes les danses māori.</p>
      <p>Étymologiquement, le mot haka signifie « faire ».</p>

      <p>Ce type de danse se pratiquait dans toute l'Océanie polynésienne.
        Il n'était pas rare de trouver dans les paroles des haka des mots crus, et des insultes à destination de l'ennemi.</p>

    <p>Aujourd’hui, le haka est défini comme la partie du répertoire de danse où les hommes sont à l’avant et les femmes à l'arrière pour le support vocal. La plupart des haka présentés aujourd’hui sont des haka taparahi ou haka sans armes.</p>

    <p>Plus que tout autre aspect de la culture māori, cette danse complexe est une expression de la passion, de la vigueur et de l'identité de ce peuple.</p>
    <p>Le haka, plus qu'un passe-temps, était une coutume d'importance, particulièrement au moment de souhaiter la bienvenue lors de rencontres sociales.
  La réputation des tribus reposait en partie sur leur habileté à faire le haka.</p>

